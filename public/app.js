async function generatePlan() {
  const apiKey = document.getElementById('api-key').value.trim();
  if (!apiKey) {
    showError(T[currentLang].error_no_key);
    document.querySelector('[data-tab="settings"]').click();
    return;
  }

  const prefs = gatherPreferences();
  const prompt = buildPrompt(prefs);

  setGenerating(true);
  hideError();

  try {
    const res = await fetch('/api/generate', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ prompt, apiKey })
    });
    const data = await res.json();
    if (!res.ok) throw new Error(data.error || 'Unknown error');

    state.lastPlan = data.result;
    renderResults(data.result, prefs);
    if (document.getElementById('auto-save').checked) savePreferences();
  } catch (err) {
    showError(`Error: ${err.message}`);
  } finally {
    setGenerating(false);
  }
}


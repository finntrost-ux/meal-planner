// ── Wizard stepper ────────────────────────────────────────────
const STEPS = ['family','cooking','nutrition','variety','restrictions','settings'];
let currentStep = 0;

function initStepper() {
  document.querySelectorAll('.wizard-step').forEach(btn => {
    btn.addEventListener('click', () => goToStep(parseInt(btn.dataset.step)));
  });
  document.querySelectorAll('.step-next-btn').forEach(btn => {
    btn.addEventListener('click', () => goToStep(currentStep + 1));
  });
  document.querySelectorAll('.step-prev-btn').forEach(btn => {
    btn.addEventListener('click', () => goToStep(currentStep - 1));
  });
  updateStepperUI();
}

function goToStep(idx) {
  if (idx < 0 || idx >= STEPS.length) return;
  currentStep = idx;
  document.querySelectorAll('.tab-panel').forEach(p => p.classList.remove('active'));
  document.getElementById(`tab-${STEPS[idx]}`).classList.add('active');
  updateStepperUI();
  document.querySelector('.config-panel').scrollIntoView({ behavior: 'smooth', block: 'start' });
}

function updateStepperUI() {
  document.querySelectorAll('.wizard-step').forEach((btn, idx) => {
    btn.classList.remove('active', 'completed');
    if (idx === currentStep) btn.classList.add('active');
    else if (idx < currentStep) btn.classList.add('completed');
  });
  document.querySelectorAll('.ws-line').forEach((line, idx) => {
    line.classList.toggle('done', idx < currentStep);
  });
}

// ── Sliders ───────────────────────────────────────────────────
function initSliders() {
  const daysSlider = document.getElementById('days-slider');
  daysSlider.addEventListener('input', () => {
    updateDayBadge();
    updateVegAuto();
  });

  const preptimeSlider = document.getElementById('preptime-slider');
  preptimeSlider.addEventListener('input', () => {
    document.getElementById('preptime-badge').textContent = `${preptimeSlider.value} min`;
  });

  const proteinSlider = document.getElementById('protein-slider');
  proteinSlider.addEventListener('input', () => {
    document.getElementById('protein-badge').textContent = `${proteinSlider.value} g`;
  });

  document.getElementById('veg-ratio').addEventListener('change', updatePlateLegend);
  document.getElementById('carb-ratio').addEventListener('change', updatePlateLegend);
}

function updateDayBadge() {
  const v = parseInt(document.getElementById('days-slider').value);
  const s = T[currentLang];
  document.getElementById('days-badge').textContent = `${v} ${v === 1 ? s.unit_day : s.unit_days}`;
  document.querySelectorAll('.day-label').forEach((l, i) => {
    l.classList.toggle('inactive', i >= v);
  });
  updateVegAuto();
}

// ── Steppers ──────────────────────────────────────────────────
function initSteppers() {
  document.querySelectorAll('.stepper-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const input = document.getElementById(btn.dataset.target);
      const dir = parseInt(btn.dataset.dir);
      const min = parseInt(input.min ?? 0);
      const max = parseInt(input.max ?? 99);
      const newVal = Math.max(min, Math.min(max, parseInt(input.value || 0) + dir));
      input.value = newVal;
      input.dispatchEvent(new Event('input', { bubbles: true }));
      if (['adults-input', 'children-list'].some(id => btn.closest(`#${id}`) || btn.dataset.target === 'adults-input')) {
        updatePortions();
      }
      updateVegAuto();
    });
  });

  document.getElementById('adults-input').addEventListener('input', updatePortions);
}


function setupTagInput(inputId, tagsId, arr) {
  const input = document.getElementById(inputId);
  const tagsEl = document.getElementById(tagsId);

  const render = () => {
    tagsEl.innerHTML = arr.map(tag => `
      <span class="tag">
        ${escHtml(tag)}
        <button data-tag="${escHtml(tag)}" aria-label="Remove ${escHtml(tag)}">✕</button>
      </span>
    `).join('');
    tagsEl.querySelectorAll('button').forEach(btn => {
      btn.addEventListener('click', () => {
        const i = arr.indexOf(btn.dataset.tag);
        if (i > -1) arr.splice(i, 1);
        render();
      });
    });
  };

  input.addEventListener('keydown', e => {
    if ((e.key === 'Enter' || e.key === ',') && input.value.trim()) {
      e.preventDefault();
      const val = input.value.replace(/,/g, '').trim();
      if (val && !arr.includes(val)) {
        arr.push(val);
        render();
      }
      input.value = '';
    }
    if (e.key === 'Backspace' && !input.value && arr.length) {
      arr.pop();
      render();
    }
  });

  document.getElementById(tagsId.replace('-tags', '-wrapper')).addEventListener('click', () => input.focus());
  render();
}

